public class BitwiseOperators {
    public static void main(String[] args) {
        System.out.println(5 & 8);
        System.out.println(6 | 7);
        System.out.println(11 ^ 9);
        System.out.println(10 >> 2);
        System.out.println(10 << 2);
        System.out.println(~-500);
    }
}
